'''
6.ACCEPT A NUMBER FROM THE USER AND PRINT IF IT IS ‘EVEN’ OR ‘ODD’
'''
n= int(input('Enter a number >'))     #enter a number to find it is even or not

if n % 2 == 0:                         #if n % 2 is equal to 0 then it will print even
    print(n,"is even number")          
else:
    print(n,"is odd number")  #if not zero then it is odd this statement will print
