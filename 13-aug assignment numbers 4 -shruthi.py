"""4.Accept a number from the user and print if the number is odd or even """


n=int(input("enter a number :"))                  #getting user input
if n%2 ==0 :                                       # if number divisible by 2 then even and printing it
    print("The entered number is even")
else:                                                  # else odd and printing it
    print("the entered number is odd")
      
